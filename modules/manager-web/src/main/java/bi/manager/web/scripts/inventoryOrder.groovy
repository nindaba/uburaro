void createOrder(String code,int quantity,long cost) {
    var order = new MBInventoryOrderData();
    order.setOrderEntry(MBInventoryEntryEnum.REFILL);
    order.setItemCode(code);
    order.setQuantity(quantity);
    order.setCost(cost);
    placeOrder(order);
}

